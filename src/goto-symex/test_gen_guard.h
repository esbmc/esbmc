#ifndef CPROVER_GOTO_SYMEX_TEST_GEN_GUARD_H
#define CPROVER_GOTO_SYMEX_TEST_GEN_GUARD_H

#include <util/message.h>

#include <filesystem>
#include <fstream>
#include <string>
#include <vector>

/// Shared no-clobber guard for the test-case generators (CTest, pytest, ...):
/// every file they write carries this on its first line, so an existing file
/// without it belongs to the user (github #6214, #6220).
inline constexpr char ESBMC_GENERATED_MARKER[] = "Auto-generated by ESBMC";

enum class overwrite_verdict
{
  safe,
  foreign,
  unreadable
};

/// An empty file is safe: a run killed mid-write leaves exactly that, and
/// refusing it would wedge generation permanently over a file with nothing in
/// it to lose.
inline overwrite_verdict classify_existing(const std::filesystem::path &path)
{
  std::ifstream in(path);
  if (!in)
    return overwrite_verdict::unreadable;
  std::string first_line;
  if (!std::getline(in, first_line))
    return overwrite_verdict::safe;
  return first_line.find(ESBMC_GENERATED_MARKER) != std::string::npos
           ? overwrite_verdict::safe
           : overwrite_verdict::foreign;
}

/// Checked up-front for every target path rather than per-file: refusing
/// halfway through would leave a half-written project behind, which is worse
/// than either writing all of it or none of it. `option_name` is the
/// command-line flag the error message suggests (e.g. "--ctest-output-dir",
/// "--pytest-output-dir").
///
/// This check is advisory, not atomic: nothing stops the target files being
/// created between here and the writes each caller performs afterward. That
/// is an acceptable trade-off for a single-shot CLI writing into a directory
/// the user controls.
inline bool can_write_all(
  const std::vector<std::filesystem::path> &paths,
  const std::string &option_name)
{
  for (const auto &path : paths)
  {
    std::error_code ec;
    if (!std::filesystem::exists(path, ec))
      continue;

    switch (classify_existing(path))
    {
    case overwrite_verdict::safe:
      break;
    case overwrite_verdict::foreign:
      log_error(
        "{} was not generated by ESBMC; refusing to overwrite it. Pass "
        "{} <dir> to generate elsewhere.",
        path.string(),
        option_name);
      return false;
    case overwrite_verdict::unreadable:
      log_error(
        "cannot read {} to check whether it is safe to overwrite; refusing. "
        "Pass {} <dir> to generate elsewhere.",
        path.string(),
        option_name);
      return false;
    }
  }
  return true;
}

#endif
