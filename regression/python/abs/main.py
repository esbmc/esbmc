# Integers
assert abs(-2) == 2
assert abs(2) == 2
assert abs(0) == 0
assert abs(-999999) == 999999
assert abs(999999) == 999999

# Floats
assert abs(-3.5) == 3.5
assert abs(3.5) == 3.5
assert abs(-0.0) == 0.0
assert abs(0.0) == 0.0

x = -2.5
assert abs(x) == 2.5

y = 1.000001
assert abs(y) == 1.000001

